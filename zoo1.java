  private static void viewAnimals() {
        StringBuilder message = new StringBuilder("Animals in the Zoo:\n");
        if (animals.isEmpty()) {
            message.append("No animals in the zoo yet.");
        } else {
            for (Animal animal : animals) {
                animal.display();
            }
        }
        JOptionPane.showMessageDialog(null, message.toString(), "View Animals", JOptionPane.INFORMATION_MESSAGE);
    }

