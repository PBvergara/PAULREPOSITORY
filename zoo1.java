import java.util.ArrayList;
import java.util.List;
import javax.swing.JOptionPane;

interface Displayable {
    void display();
}
abstract class Animal implements Displayable {
    protected String name;
    protected int age;

    public Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }
}
class Reptile extends Animal {
    private double length;

    public Reptile(String name, int age, double length) {
        super(name, age);
        this.length = length;
    }
     @Override
    public void display() {
        String message = "Reptile: Name - " + name + ", Age - " + age + ", Length - " + length;
        JOptionPane.showMessageDialog(null, message, "View Animals", JOptionPane.INFORMATION_MESSAGE);
    }
}
class Bird extends Animal implements CanFly {
    private String type;
    private double wingspan;

    public Bird(String name, int age, String type, double wingspan) {
        super(name, age);
        this.type = type;
        this.wingspan = wingspan;
    }

    @Override
    public void display() {
        String message = "Bird: Name - " + name + ", Type - " + type + ", Age - " + age + ", Wingspan - " + wingspan;
        JOptionPane.showMessageDialog(null, message, "View Animals", JOptionPane.INFORMATION_MESSAGE);
    }

    @Override
    public void fly() {
        JOptionPane.showMessageDialog(null, name + " is flying.", "Flying", JOptionPane.INFORMATION_MESSAGE);
    }
}
class Aquatic extends Animal {
    private String type;

    public Aquatic(String name, int age, String type) {
        super(name, age);
        this.type = type;
    }

    @Override
    public void display() {
        String message = "Aquatic Animal: Name - " + name + ", Age - " + age + ", Type - " + type;
        JOptionPane.showMessageDialog(null, message, "View Animals", JOptionPane.INFORMATION_MESSAGE);
    }
}
interface CanFly {
    void fly();
}
